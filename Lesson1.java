public class Main {
    public static void main(String[] args) {
        // Это базовые концепции языка
        System.out.println("Hello, World !"); // Теперь следий вывод будет с новой строки
        System.out.print("Hello, World !\n"); // Тоже перевод на новую строку(можно вставлять между словами)
        System.out.print("Hello \t World !"); // Табуляция

        /*

        Это многострочный комментарий
        Да, много строк :)

        */
    }
}
